async function main() {
  console.log("Starting analysis injection to BOTH tables (Track & TrackHonban)...");
  for (const item of analyses) {
    try {
      // Update Track table
      await prisma.track.update({
        where: { timestamp: item.timestamp },
        data: { 
          analysis: item.analysis,
          published: true,
          entryNo: item.entryNo
        }
      });
      console.log(`Updated Track: ${item.entryNo}`);

      // Update TrackHonban table
      await prisma.trackHonban.update({
        where: { timestamp: item.timestamp },
        data: { 
          analysis: item.analysis,
          published: true,
          entryNo: item.entryNo
        }
      });
      console.log(`Updated TrackHonban: ${item.entryNo}`);

    } catch (error) {
      console.error(`Failed to inject/update ${item.timestamp}:`, error.message);
    }
  }
  console.log("Analysis injection and publishing completed!");
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
