import { PrismaClient } from '@prisma/client';
const prisma = new PrismaClient();

async function updateAnalysis() {
  // 1. 最新の楽曲データを取得
  const track = await prisma.track.findFirst({
    where: { title: 'ノン_クレジット_フィクサー' }, // 部分一致やタイトルで指定
  }) || await prisma.track.findFirst({
    where: { id: 1 } // IDで指定
  });

  if (!track) {
    console.log('❌ 対象の楽曲が見つかりませんでした。');
    return;
  }

  console.log(`🎵 「${track.title}」の解析を生成・反映中...`);

  // 2. 指定のフォーマットで解析文を作成 (ユーザー様から共有いただいた内容をベースに最新歌詞を確認)
  const analysisContent = `### 🌐 テーマ分析
この楽曲の核心は、**「姿なきクリエイター＝影の立役者の美学」**にあります。
作品の完成を支えながらも、自らはエンドロールに名を連ねることのない（ノン・クレジット）、AIツールやプラグイン、あるいは無名の貢献者。そんな「見えないフィクサー」としての矜持と、作品を純粋に楽しむ人々への愛がテーマとなっています。

### ⚙️ 歌詞の工夫点
* **「ミルク」の比喩**: コーヒーに溶け込み、姿は見えなくなっても味を完成させるミルクを「フィクサー」に例える構成が非常に秀逸です。
* **創作タームの融合**: 「エンドロール」「Cパート」といった映像用語と、「マウス」「クリック音」といった制作現場のリアリティを同居させることで、メタ的な視点と現場の温もりを両立させています。
* **「隠し味」としての誇り**: 種明かしを望まず、「おいしかった」という何気ない感想を最高のご褒美とする姿勢に、奥ゆかしくも強いプロフェッショナリズムを感じさせます。

### 🎤 注目すべきパンチライン
> **「誰かの発明品　当たり前すぎて　「いつも、ありがとう」　どこに言えばいいんだろう？」**

（解説）
私たちが日常的に享受している最新技術や、見えない誰かの「発明」に対する切実な感謝が、この一節に凝縮されています。便利さが当たり前になった現代において、その背後にある「顔のない善意」に光を当てる、この楽曲で最も美しく鋭いフレーズです。`;

  // 3. データベースを更新
  await prisma.track.update({
    where: { id: track.id },
    data: { analysis: analysisContent }
  });

  console.log('✅ 解析データの反映が完了しました。');
}

updateAnalysis()
  .catch(err => console.error('❌ エラーが発生しました:', err))
  .finally(() => prisma.$disconnect());
