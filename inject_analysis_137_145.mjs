async function main() {
  for (const item of analyses) {
    const track = await prisma.trackHonban.findFirst({
      where: { entryNo: item.entryNo }
    });

    if (track) {
      await prisma.trackHonban.update({
        where: { id: track.id },
        data: { analysis: item.analysis }
      });
      console.log(`Updated analysis for No. ${item.entryNo}: ${track.title}`);
    } else {
      console.warn(`Track No. ${item.entryNo} not found.`);
    }
  }
}

main().catch(console.error).finally(() => prisma.$disconnect());
