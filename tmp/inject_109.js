const { PrismaClient } = require('@prisma/client');
const prisma = new PrismaClient();

async function main() {
  const markdown = `🌐 テーマ分析
本作は、染色体の末端にあり細胞分裂のたびに短縮していく命の回数券「テロメア」を題材に、不可逆な時間と自己実現への焦燥を描き出す哲学的なサイバー・ポエトリーだ。生物学的な寿命のカウントダウンを、あえて「蒸気機関車」という無骨で熱量の高い機械に例えることで、静かに、しかし確実に消費されていく命の力強さと残酷さを浮き彫りにしている。限られた時間の中で、ただ無為に生きるのではなく「何を残すか」を突きつける、鋭利なメッセージ性が光る。

⚙️ 歌詞の工夫点
特筆すべきは、全編を貫く「-a」の徹底した脚韻（テロメア、教えてや、ナイトメア、デザイア等）が生み出す、呪術的とも言えるリズム感である。この反復構造が、細胞分裂（分裂、分裂、分裂停止）の法則性や、止まることのない蒸気機関車の駆動音と見事にシンクロしている。「みじゅせいからの、細胞分裂」という無機質な生物学用語のリフレインが、かえって生々しい焦燥感を煽り、聴き手の意識下にある「死への恐怖」を直接的に揺さぶる巧妙な言葉遊びが展開されている。

🎤 注目すべきパンチライン
「ただ夢見ちゃいないか / 形にしていけよ君のデザイア」
命の終わり（ナイトメア）を淡々と提示してきた楽曲が、突如として熱を帯びる覚醒のライン。テロメアの消費は単なる「死へのカウントダウン」ではなく、自らの欲望（デザイア）を形にするための「命の燃焼」なのだと鮮やかに視点を反転させる。いつか来る「納得のエンド」へ向けて、傍観者であることを許さない、圧倒的な熱量を持った魂への痛烈なアジテーションである。`;

  // DBの「entryNo」が '109' のレコードを更新
  const updated = await prisma.trackHonban.updateMany({
    where: { entryNo: '109' },
    data: { analysis: markdown }
  });
  
  console.log(`Updated ${updated.count} records for No.109`);
}

main().catch(console.error).finally(() => prisma.$disconnect());
