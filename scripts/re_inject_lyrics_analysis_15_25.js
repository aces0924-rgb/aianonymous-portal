
const { PrismaClient } = require('@prisma/client');
const prisma = new PrismaClient();

async function updateAnalysis(no, analysisText) {
  try {
    const entryNoStr = String(no).padStart(3, '0');
    const result = await prisma.trackHonban.updateMany({
      where: { entryNo: entryNoStr },
      data: { analysis: analysisText },
    });
    console.log(`[Success] No.${entryNoStr} re-updated (${result.count} records).`);
  } catch (error) {
    console.error(`[Error] Failed to update No.${no}:`, error.message);
  }
}

